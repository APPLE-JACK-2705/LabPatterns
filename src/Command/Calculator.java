package Command;

public class Calculator {
    // Сумма
    public void plus(float left, float right){
        System.out.println(left + right);
    }

    // Разность
    public void minus(float left, float right){
        System.out.println(left - right);
    }

    // Умножение
    public void mult(float left, float right){
        System.out.println(left * right);
    }

    // Деление
    public void div(float left, float right){
        System.out.println(left / right);
    }
}
